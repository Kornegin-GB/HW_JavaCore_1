package ru.geekbrains.controller;

public class Decor {
    /**
     * Функция форматированного вывода результата
     *
     * @param a результат вычисления
     * @return Строка форматированного вывода
     */
    public String decoration(int a) {
        return "Результат вычисления: " + a;
    }
}
